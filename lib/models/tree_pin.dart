class TreePin {
  const TreePin({
    required this.title,
    required this.locationName,
    required this.datePlanted,
    required this.description,
    required this.plantedBy,
    required this.x,
    required this.y,
  });

  final String title;
  final String locationName;
  final String datePlanted;
  final String description;
  final String plantedBy;
  final double x;
  final double y;
}
